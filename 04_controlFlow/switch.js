const month = 3;

switch (month) {
    case 1:
        console.log("January")
        
        break;
    case 2:
        console.log("Feb")
        
        break;
    case 3:
        console.log("March")
        
        break;
    case 4:
        console.log("April")
        
        break;
    case 5:
        console.log("May")
        
        break;
    
    default:
        console.log("Default Case")
        break;
}
// if case while cases not have breaks it will run all expect Default case
// we use switch for other datatype also like string and all
